package Lesson2.Demo;

public class DemoSwitchCase {
    public static void main(String[] args) {
    int number = 2;
    switch (number) {
        case 1:
            System.out.println("Case 1");
            break;
        case 2:
            System.out.println("Case 2");
            break;
        case 3:
            System.out.println("Case 3");
            break;
        default:
            System.out.println("Default");
        }
    }
}
